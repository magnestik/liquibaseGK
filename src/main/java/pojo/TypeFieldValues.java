package pojo;


public class TypeFieldValues {

  private long fieldValueId;
  private long contractId;
  private long typeFieldId;
  private String value;


  public long getFieldValueId() {
    return fieldValueId;
  }

  public void setFieldValueId(long fieldValueId) {
    this.fieldValueId = fieldValueId;
  }


  public long getContractId() {
    return contractId;
  }

  public void setContractId(long contractId) {
    this.contractId = contractId;
  }


  public long getTypeFieldId() {
    return typeFieldId;
  }

  public void setTypeFieldId(long typeFieldId) {
    this.typeFieldId = typeFieldId;
  }


  public String getValue() {
    return value;
  }

  public void setValue(String value) {
    this.value = value;
  }

}
